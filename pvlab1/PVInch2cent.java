public class PVInch2cent { // start class
    public static void main(String[] args) { // start main
        double inch = 1; // set decimal value for inch as 1
        double centimeters = inch * 2.54; // formula for inch->cm conversion
        System.out.println("1 inch equals " + centimeters + " centimeters."); // display 1 inch in centimeters
    } // end main
} // end class
